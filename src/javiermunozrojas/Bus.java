package javiermunozrojas;

public class Bus {

	private String label;
	private String nameA;
	private String nameB;
	
	public Bus(String label, String nameA, String nameB) {
		super();
		this.label = label;
		this.nameA = nameA;
		this.nameB = nameB;
	}

	public String getLabel() {
		return label;
	}

	public String getNameA() {
		return nameA;
	}

	public String getNameB() {
		return nameB;
	}
	
	
	
	
}
